package com.example.demo.bean;

public class Airline {
    private String Airline_Code;
    private String Airline_Name;
    private String Airline_OwnDiscount;

    public String getAirline_Code() {
        return Airline_Code;
    }

    public void setAirline_Code(String airline_Code) {
        this.Airline_Code = airline_Code;
    }

    public String getAirline_Name() {
        return Airline_Name;
    }

    public void setAirline_Name(String airline_Name) {
        this.Airline_Name = airline_Name;
    }

    public String getAirline_OwnDiscount() {
        return Airline_OwnDiscount;
    }

    public void setAirline_OwnDiscount(String airline_OwnDiscount) {
        this.Airline_OwnDiscount = airline_OwnDiscount;
    }
}
